
// MODULUS OPERATOR "%"

// after division, modulus operator gives remaining part

#include <stdio.h>

int main()
{
    int division_result = 11 / 5; // 2
    int modulus_result = 11 % 5;  // 1

    printf("\n%d\n", division_result);
    printf("\n%d\n", modulus_result);

    return 0;
}
